function rock() {
  let weapons = ['ROCK', 'PAPER', 'SCISSORS'];
  let weapon = weapons[Math.floor(Math.random()*weapons.length)];
  if (weapon == 'ROCK') {
    return document.getElementById("result").innerHTML = 'Computer chose: ' + weapon;
  }
  else if (weapon == 'PAPER') {
    return document.getElementById("result").innerHTML = 'Computer chose: ' + weapon;
  }
  else {
    return document.getElementById("result").innerHTML = 'Computer chose: ' + weapon;
  }
}

